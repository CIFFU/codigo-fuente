  La investigación en Física de Partículas Elementales y Relatividad General, que se inició en la BUAP a finales de los años 70, ha sido sin lugar a dudas una de las áreas más productivas.
Dentro de esta línea se ha mantenido un alto ritmo de publicación de artículos en revistas del más alto nivel. Se incluyen también el desarrollo del área experimental, a la fecha la BUAP cuenta con uno de los cuatro grupos más activos que hay en el país y mantiene una colaboración con experimentos de carácter nacional e internacional.

<br>
