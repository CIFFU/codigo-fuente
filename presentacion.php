    <h4 class="titulo-presentacion" id="queeselciffu" data-magellan-target="queeselciffu">Resumen</h4>

      <p>
        En este documento se presenta una propuesta para la creación de un centro de investigación de tipo semi-virtual, especializado en Física Fundamental dentro de la BUAP, con un enfoque multidisciplinario, donde confluyan esfuerzos coordinados para realizar investigación de frontera, formación de recursos humanos de alto nivel y acciones para la difusión de la ciencia con incidencia en el sistema educativo nacional. Esta propuesta incluye la participación de varios especialistas que provienen de diversas dependencias de la BUAP con intereses afines, se cuentan con una serie  de colaboradores nacionales e internacionales, así como estudiantes de licenciatura y postgrado e investigadores postdoctorantes. Se proponen una serie de acciones para mantener e incrementar la colaboración  nacional e internacional de alto impacto, con el objetivo de lograr un reconocimiento global y proyectar a la BUAP como una institución líder de México y Latinoamérica en el campo de la Física Fundamental, para beneficio de los profesores, estudiantes y de la misma sociedad.
      </p>

      <p>

        El proyecto se propone  coordinar la colaboración de los profesores-investigadores de la BUAP que trabajan en temas de Física Fundamental, incluidas  las áreas de Partículas y Campos, Cosmología, Astrofísica y aspectos básicos de la Teoría Cuántica de la BUAP, interesados todos en desarrollar investigación básica orientada a resolver problemas de frontera.

        Asimismo, se propone realizar labores de consultoría dentro y fuera de la BUAP, para el estudio de problemas nacionales, buscando aplicar los desarrollos científicos y tecnológicos asociados con estas áreas del conocimiento. En particular, se propone continuar con el estudio de los temas de Física de Partículas Elementales con mayor tradición en la BUAP (bosón de Higgs, quark top, materia oscura y rayos cósmicos), incluyendo la formulación de nuevos modelos de partículas, así como el estudio de sus implicaciones experimentales en colisionadores y el Cosmos. El centro se propone participar en labores de divulgación de la ciencia y estudiar la inserción del nuevo conocimiento en los programas educativos del país.

      </p>

      <p>
        Es de todos conocido que la WEB se desarrolló en un centro de investigación de física de aceleradores de partículas, el CERN, y que tuvo una gran incidencia para el desarrollo de las comunicaciones a nivel mundial. Así como esta, existen muchas otras posibilidades de desarrollo que se pueden realizar desde la ciencia básica. La WEB marchó, prácticamente por sí misma, pero implementar otras posibilidades depende de contar con grupos de investigación en colaboración permanente con los centros de investigación y tecnológicos consolidados del mundo, así como los emergentes, que permitan alinear el trabajo local con las tendencia  internacionales mas modernas .
      </p>

      <p>
        En nuestro país, no solo debemos ser testigos del desarrollo científico y tecnológico mundial, sino que debemos participar activamente en estas labores con los recursos humanos con los que contamos. Para esto la BUAP requiere de un instrumento que le permita conocer y evaluar los niveles de desarrollo alcanzados hasta el presente, en estas áreas del conocimiento. A través del Centro Internacional de Física Fundamental  de la BUAP (CIFFU-BUAP) se puede elaborar un diagnóstico continuo sobre la situación actual y las tendencias mundiales en la generación de conocimiento. Mediante la organización de una serie de foros consultivos  con los investigadores del área de física fundamental, se podrían identificar las directrices a seguir en el presente inmediato.  Identificar nuevas formas de contactos, comunicaciones, regularización y normativa para la investigación científica y tecnológica en nuestra Institución más allá de las formas tradicionales.
      </p>



<h4 class="titulo-presentacion" id="mision" data-magellan-target="mision">Presentación</h4>
      <p>
        La propuesta de creación de un centro de investigación semi-virtual especializado en Física Fundamental, Teórica y experimental, contemplando sus conexiones multi-disciplinarias, se inscribe dentro de la tendencia mundial para organizar el trabajo de grupos de alto impacto. Tales centros se han creado en muy variados contextos, en algunos casos están ubicados dentro de las instituciones de mayor prestigio que buscan mantenerse a la vanguardia, pero también se han fundado en los países de economías emergentes, ávidos de lograr una mayor participación e impacto internacional para sus grupos de científicos más consolidados.
      </p>

      <p>
        El grado de madurez que han alcanzado muchos de los profesores e investigadores en Física de Altas Energías dentro la BUAP, los cuales conforman el grupo que hace esta propuesta, descansa en un trabajo sostenido por más de veinte años, en temas que cubren desde la Física del bosón de Higgs y el quark top, los rayos cósmicos hasta extensiones del Modelo Estándar que incluyen candidatos para la materia oscura; Dicho trabajo incluso ha logrado un reconocimiento internacional. Por lo cual, pensamos que fundar un centro de investigación científica de este tipo dentro de la BUAP, es un camino adecuado y asequible para pasar a una etapa superior, que permitiría potenciar la interfase entre la Física Fundamental con los Colisionadores y el Cosmos, con una participación de alto perfil en las colaboraciones internacionales, abocada a la solución de problemas de frontera.
      </p>

      <p>
        La misión del centro CIFFU-BUAP será propiciar la internacionalización del trabajo de investigación y formación  de recursos humanos en el área de Física, mediante el cual se espera lograr que los estudiantes e investigadores trabajen en un ambiente académico global. Las áreas de investigación incluyen aspectos básicos y la formulación de extensiones del Modelo Estándar, incluyendo la predicción de señales experimentales en los aceleradores de partículas actuales (LHC)
y futuros (FCC, VLHC), así como diversos aspectos formales de
Teoría Cuántica de Campos y la conexión con la Física de Astropartículas, áreas en las cuales el grupo
cuenta con una amplia experiencia.
      </p>

      <p>
        Las acciones encaminadas a lograr
lo anterior, incluyen la formulación de
proyectos de investigación con un alto
componente internacional, que tengan
como meta insertar líneas originales de
investigación. Para ello se propone acompañar los proyectos con un programa de visitas de investigadores de alto perfil nacionales e inter-
nacionales, durante el año académico, así
como la organización de eventos académicos (talleres, escuelas y simposios), acciones que fomenten la interacción de los estudiantes con investigadores invitados (nacionales y de instituciones extranjeras). Todo lo anterior debe redundar en la formación de recursos humanos de alta calidad académica que compitan internacionalmente, fomentando su participación en eventos y proyectos internacionales.
      </p>

      <p>
        Además, de acuerdo al Plan de Desarrollo Institucional, se plantea promover la internacionalización de nuestros programas de posgrado, estudiantes e investigadores, mediante diversas acciones, que incluirían: la movilidad de estudiantes de alto perfil académico en instituciones o centros de investigación líderes; firma de convenios internacionales con las instituciones con las que se tiene colaboración. También se incluye promover estancias de investigación por periodos de uno a doce meses para investigadores de instituciones internacionales que impartirán cursos y avanzados proyectos investigación. Se propone discutir y proponer medidas que fomenten la captación de estudiantes extranjeros.
      </p>

      <p>
        Asimismo, para aprovechar las experiencias mas haya de ámbito puramente académico, el CIFFU podría actuar como asesor institucional de apoyo a las autoridades universitarias en temas afines a la investigación científica, en general, y de la Física Fundamental y de Altas Energías en particular. Como parte de la vinculación se plantea realizar estudios para el desarrollo de innovaciones educativas y difusión de la ciencia, así como la elaboración de material educativo. Participar en la elaboración de un plan de educación científica que sea considerado dentro de los planes de estudio de nivel básico y medio superior en el país.
      </p>

      <p>
        Cabe mencionar como aspecto innovador en México, que forma parte de la visión del CIFFU, es implementar un diálogo entre las áreas de teoría y experimento de diversas subdisciplinas. De hecho, un centro como el que se propone, sería el primero de su tipo en Latinoamérica, lo cual sería evidencia del liderazgo de la BUAP en el campo.
      </p>

