La investigación en Física de Partículas Elementales y Relatividad General, que se inició en la BUAP a finales de los años 70, ha sido sin lugar a dudas una de las áreas más productivas en la institución. Dentro de esta línea se ha mantenido un alto ritmo de publicación de artículos en revistas del más alto nivel. Se incluyen también el desarrollo del área experimental, a la fecha la BUAP cuenta con uno de los cuatro grupos más activos que hay en el país, se mantiene una colaboración con experimentos de carácter nacional e internacional.
<br>
 <br>

 El grado de madurez de este grupo lo demuestra el alto número de miembros del SNI, mientras que la calidad de la investigación se ilustra por el número de artículos de alto impacto. Asimismo, el grupo se ha involucrado en actividades de liderazgo en el país, como lo son la organización de eventos académicos, participación en comisiones para la directriz de la ciencia en México, por ejemplo con la participación en comisiones de premios de la Academia Mexicana de Ciencias, la Comisión Evaluadora del SNI y de comisiones de Proyectos del CONACyT.

