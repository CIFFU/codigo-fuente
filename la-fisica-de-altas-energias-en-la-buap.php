El grado de madurez de este grupo lo demuestra el alto número de miembros del SNI, mientras que la calidad e impacto de la investigación se ilustra por el número de artículos con más de 50 o 100 citas. Asimismo, el grupo se ha involucrado en actividades de liderazgo en el país, como lo son la organización de eventos académicos, participación en comisiones para la directriz de la ciencia en México, por ejemplo con la participación en comisiones de premios de la Academia Mexicana de Ciencias, la Comisión Evaluadora del SNI y de la Comisión de Proyectos del CONACyT.
