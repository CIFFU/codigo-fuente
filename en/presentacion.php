    <h4 class="titulo-presentacion" id="queeselciffu" data-magellan-target="queeselciffu">Summary</h4>

      <p>
        As already mentioned, CIFFU is a semi-virtual research center specialized in Fundamental Physics within the BUAP, with a multidisciplinary approach whose main objectives are:
      <ul>
        <li>Coordinate efforts to conduct frontier research</li>
        <li>Develop high-level human resources</li>
        <li>Disseminate science procuring high impact on the national educational system</li>
      </ul>

        The CIFFU includes the participation of several specialists who come from different dependencies of BUAP with similar interests, has a number of national and international collaborators, as well as undergraduate and postgraduate students and postdoctoral researchers. It proposes a series of actions to maintain and increase high impact national and international collaboration, with the objective of achieving global recognition and projecting BUAP as a leading institution in Mexico and Latin America in the field of Fundamental Physics, for the benefit of teachers, students and society in general.

      </p>

      <p>

        The project proposes to coordinate the collaboration of the BUAP professors-researchers working on fundamental physics topics, including the areas of Particles and Fields, Cosmology, Astrophysics and basic aspects of Quantum Theory, all interested in developing basic research aimed at solving frontier problems. Likewise, it proposes to carry out consultancy work inside and outside BUAP to study national problems, seeking to apply the scientific and technological developments associated with these areas of knowledge. It proposes to extend the study of the subjects of Elementary Physics of greater tradition in the BUAP - Higgs boson, top quark, dark matter and cosmic rays -, including the formulation of new models of particles, as well as the study of Its experimental implications on colliders and the Cosmos. The center proposes to participate in the dissemination of science and to study the insertion of new knowledge into the educational programs of the country.

      </p>

      <p>
        It is well known that the WEB was developed at a research center of particle physics, the CERN, and that it had a great impact for the development of communications worldwide. Just like this, there are many other possibilities for development that can be realized from basic science. The WEB marched, practically on its own, but implementing other possibilities depends on having research groups in permanent collaboration with the consolidated research and technological centers of the world, as well as the emerging ones, that allow to align the local work with the modern international trends.
      </p>

      <p>
        In our country, not only must we witness global scientific and technological development, but we must actively participate in these tasks with the human resources we have. In order to achieve this objective, the BUAP requires an instrument that allows it to know and evaluate the levels of development achieved so far, in these areas of knowledge. Through the BUAP's International Center for Fundamental Physics (CIFFU-BUAP), a continuous diagnosis is made on the current situation and global trends in knowledge generation. By organizing a series of consultative forums with researchers in the field of fundamental physics, CIFFU identifies the guidelines to follow in the immediate present. The CIFFU allows to discover and nurture new forms of contacts, communications, regulation and regulation for scientific and technological research in our Institution beyond the traditional forms.
      </p>



<h4 class="titulo-presentacion" id="mision" data-magellan-target="mision">Presentation</h4>
      <p>
      The CIFFU is part of the global trend to organize high-impact working groups. There are several centers created in many different contexts. In some cases they are located within the most prestigious institutions that seek to remain at the forefront, but they have also been based in countries of emerging economies, eager to achieve greater participation and international impact for their more consolidated groups of scientists.
      </p>

      <p>
      The degree of maturity achieved by many of the professors and researchers in High Energy Physics within BUAP, who integrate the group at the CIFFU, is based in continuous efforts sustained for more than twenty years, in subjects that cover  Higgs boson and top quark physics, from cosmic rays to extensions of the Standard Model, including candidates for dark matter. This work has even achieved some international recognition. Therefore, we believe that encouraging a scientific research center of this type within the BUAP is an adequate and affordable way to proceed to a higher stage, which would enable the interface between the Fundamental Physics with the Colliders and the Cosmos to be enhanced and focused on solving frontier problems, with a high-profile participation in international collaborations.
      </p>

      <p>
        The mission of the CIFFU-BUAP center is to foster the internationalization of research and human resources training in the field of Physics, through which students and researchers are expected to work in a global academic environment. Research areas include basic aspects and formulation of extensions of the Standard Model, including prediction of experimental signals in current (LHC) and future particle accelerators (FCC, VLHC), as well as various formal aspects of Quantum Field Theory and its connection with the Physics of Astroparticles, areas in which the group has extensive experience.
      </p>

      <p>
        The actions aimed to this end, include the formulation of research projects with a high international component, with the goal of inserting original lines of research. For this purpose, it is proposed to accompany the projects with a program of visits of high-profile national and international researchers during the academic year, as well as the organization of academic events (workshops, schools and symposia), actions that encourage the interaction of students with invited researchers (national and foreign institutions). All the above results in the training of high quality academic human resources that compete internationally, encouraging their participation in international events and projects.
      </p>

      <p>
        In addition, in accordance with the Institutional Development Plan, CIFFU promotes the internationalization of our postgraduate programs, students and researchers, through various actions, including: mobility of high-profile students in leading research institutions or centers, signing of international agreements with the institutions with which we have collaboration. It also includes the promotion of research stays for periods of one to twelve months for researchers from international institutions to participate in courses and advanced research projects. Likewise, measures to encourage the recruitment of foreign students are discussed and proposed.
      </p>

      <p>
        To take advantage of experiences beyond purely academic scope, CIFFU acts as an institutional adviser supporting university authorities in matters related to scientific research in general, and of Fundamental Physics and High Energies in particular. As part of the linkage, studies are carried out for the development of educational innovations and dissemination of science, as well as the elaboration of educational material. The CIFFU participates in the elaboration of a plan of scientific education that is considered within the curricula of basic level and superior means in the country.
      </p>

      <p>
        It is worth mentioning as an innovative aspect in Mexico, which is part of CIFFU's vision, is the implementation of a dialogue between the areas of theory and experiment of various subdisciplines. In fact, a center like the one proposed would be the first of its kind in Latin America, which would be evidence of BUAP's leadership in the field.
      </p>

