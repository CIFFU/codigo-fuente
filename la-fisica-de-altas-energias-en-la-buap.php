La incidencia de nuestro grupo en la formación de recursos humanos se ilustra por el alto número de estudiantes tesistas de licenciatura y postgrado que se han formado en nuestro grupo, que a su vez han contribuido al desarrollo de la investigación en diversas instituciones del país.

<br>

También, a lo largo de estos años se han obtenido importantes apoyos y recursos del exterior, que han beneficiado a la BUAP, ya sea mediante proyectos de CONACYT, de la fundación México-USA para la ciencia (UC-Mexus), de la Comunidad Europea (HELEN). Otra medida del reconocimiento del alto nivel de las actividades del grupo, lo constituye el número de eventos académicos internacionales que han organizado los miembros del grupo. Desde hace varios años, se han establecido colaboraciones con grupos internacionales diversos y se ha participado en los grupos de trabajo en temas de frontera. Además, el trabajo realizado en el tema de rompimiento de las simetrías en la Física de Partículas, tanto en el sector electrodébil como del sabor, han sido temas que se han trabajado desde el punto de vista teórico en el grupo, entre otros. Los resultados de este trabajo han sido considerados por los experimentos más importantes de la Física de Partículas, como son los relacionados con el LHC del CERN, donde se logró detectar el bosón de Higgs.

